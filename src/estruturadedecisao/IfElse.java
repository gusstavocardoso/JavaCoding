package estruturadedecisao;

public class IfElse {
    public static void main (String[] args){
        int num = 3;

        if(num > 5){
            System.out.println("Maior que 5");
        }else if(num == 5){
            System.out.println("Igual a 5");
        }else{
            System.out.println("Menor que 5");
        }
    }
}
